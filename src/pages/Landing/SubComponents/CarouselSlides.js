const CarouselSlides = (props) => {
    const [activeIndex, setActiveIndex] = useState(0);
    const [animating, setAnimating] = useState(false);

    const next = () => {
        if (animating) return;
        const nextIndex = activeIndex === items.length - 1 ? 0 : activeIndex + 1;
        setActiveIndex(nextIndex);
    }

    const previous = () => {
        if (animating) return;
        const nextIndex = activeIndex === 0 ? items.length - 1 : activeIndex - 1;
        setActiveIndex(nextIndex);
    }

    const goToIndex = (newIndex) => {
        if (animating) return;
        setActiveIndex(newIndex);
    }

    const slides = items.map((item, i) => {
        return (
            <CarouselItem
                onExiting={() => setAnimating(true)}
                onExited={() => setAnimating(false)}
                key={item.img}
            >
                <Container>
                    <Row className="align-items-center mt-5 mt-md-0">
                        <Col md="6">
                            {item.header}
                            {item.text}

                            <DownloadImages />
                        </Col>
                        <Col md="6">
                            {/* <img src={require('../../../assets/images/svgs/CarouselWoman.svg').default} alt="happy shopper" height="600" className="w-100" /> */}
                            <img src={require(`../../../assets/images/svgs/Slider${item.img}.svg`).default} alt="happy shopper" height="600" className="w-100" />
                            <div className="number-footer float-right">
                                <h3> <span className="text-yellow">{i+1}</span>/{items.length}</h3>
                            </div>
                        </Col>
                    </Row>
                </Container>
                {/* <CarouselCaption captionText={item.caption} captionHeader={item.caption} /> */}
            </CarouselItem>
        );
    });

    return (
        <div id="slider" className="">
            <Carousel
                activeIndex={activeIndex}
                next={next}
                previous={previous}
            >
                <CarouselIndicators items={items} activeIndex={activeIndex} onClickHandler={goToIndex} />
                {slides}
                <CarouselControl direction="prev" directionText="Previous" onClickHandler={previous} />
                <CarouselControl direction="next" directionText="Next" onClickHandler={next} />
            </Carousel>
        </div>
    );
}

export default CarouselSlides;
